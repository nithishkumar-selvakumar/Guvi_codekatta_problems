#include<stdio.h>
void main()
{
	int n;
	printf("enter the number:");
	scanf("%d",&n);
	switch(n)
	{
		case 1:
		print("One");
		break;
		case 2:
		print("Two");
		break;
		case 3:
		print("Three");
		break;
		case 4:
		print("Four");
		break;
		case 5:
		print("Five");
		break;
		case 6:
		print("Six");
		break;
		case 7:
		print("Seveen");
		break;
		case 8:
		print("Eight");
		break;
		case 9:
		print("Nine");
		break;
		case 0:
		print("Zero");
		break;
		default:
			printf("no such number");
	}
}
